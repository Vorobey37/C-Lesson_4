void PrintArray(int[,] array)
{
    for (int i = 0; i < array.GetLength(0); i++)
    {
        for (int j = 0; j < array.GetLength(1); j++)
        {
            if (array[i, j] == 0)
            {
                Console.Write($" ");
            }
            else
            {
                Console.Write($"+");
            }
        }
        Console.WriteLine();
    }
}

void FillArray(int row, int column)
{
    if (pic[row, column] == 0)
    {
        pic[row, column] = 1;
        FillArray(row - 1, column);
        FillArray(row, column - 1);
        FillArray(row + 1, column);
        FillArray(row, column + 1);
    }
}

